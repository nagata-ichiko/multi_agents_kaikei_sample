export function Sidebar() {
  const pathname = usePathname();

  const isActive = (href: string) => {
    if (href === "/") return pathname === "/";
    return pathname.startsWith(href);
  };

  return (
    <aside className="fixed inset-y-0 left-0 z-30 w-56 bg-slate-900 flex flex-col">
      {/* Logo */}
      <div className="flex h-16 items-center px-5 border-b border-slate-700">
        <span className="text-white font-bold text-lg tracking-tight">
          Kaikei
        </span>
        <span className="ml-1.5 text-indigo-400 text-xs font-medium">会計</span>
      </div>

      {/* Navigation */}
      <nav className="flex-1 overflow-y-auto py-4 px-3">
        {navSections.map((section) => (
          <div key={section.label} className="mb-4">
            <p className="px-2 mb-1 text-xs font-semibold text-slate-500 uppercase tracking-wider">
              {section.label}
            </p>
            <ul className="space-y-0.5">
              {section.items.map((item) => {
                const active = isActive(item.href);
                return (
                  <li key={item.href}>
                    <Link
                      href={item.href}
                      className={[
                        "flex items-center gap-3 px-2 py-2 rounded-lg text-sm font-medium transition-colors",
                        active
                          ? "bg-indigo-600 text-white"
                          : "text-slate-400 hover:bg-slate-800 hover:text-slate-100",
                      ].join(" ")}
                    >
                      {item.icon}
                      {item.label}
                    </Link>
                  </li>
                );
              })}
            </ul>
          </div>
        ))}
      </nav>
    </aside>
  );
}
